def _are_adjacent(a: ObjFact, b: ObjFact) -> bool:
    """Check if two objects are 4-adjacent (touching but not overlapping)."""
    # Vertical adjacency
    if (abs(a.row - (b.row + b.height)) <= 1 or abs(b.row - (a.row + a.height)) <= 1):
        if not (a.col + a.width <= b.col or b.col + b.width <= a.col):
            return True
    # Horizontal adjacency
    if (abs(a.col - (b.col + b.width)) <= 1 or abs(b.col - (a.col + a.width)) <= 1):
        if not (a.row + a.height <= b.row or b.row + b.height <= a.row):
            return True
    return False


def _is_contained(inner: ObjFact, outer: ObjFact) -> bool:
    """Check if inner's bbox is fully inside outer's bbox."""
    return (outer.row <= inner.row and
            outer.col <= inner.col and
            outer.row + outer.height >= inner.row + inner.height and
            outer.col + outer.width >= inner.col + inner.width and
            outer.size > inner.size)
